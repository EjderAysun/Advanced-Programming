a = 4
b = 3
print(-b)
# Output: -3

print(a + b)
# Output: 7

print(a / b)
# Output: 1.3333333333333333

print(a % b)
# Output: 1

print(a**b)
# Output: 64

print(10 / 5)
# Output: 2.0

print(type(10/5))
# Output: <class 'float'>

print(2 * 25 + 30)
# Output: 80

